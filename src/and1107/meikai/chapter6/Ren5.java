package and1107.meikai.chapter6;

public class Ren5
{

	public static void main(String[] args)
	{
		String s = "繰り返し文だ～";

		for (int i = 0; i < 3; i++) {
			System.out.println(s);
		}
		System.out.println();

		int j = 0;

		while (j < 3) {
			System.out.println(s);

			j++;
		}

	}
}
