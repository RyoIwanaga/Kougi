public class Ex1 {

	public static void main(String[] args) {

		double r = 10.0;
		double circumference = 2 * Math.PI * r;
		double circle_area = r * r * Math.PI;

		System.out.println("円周（英訳：circumference）= " + circumference);
		System.out.println("円の面積 = " + circle_area);
	}
}
